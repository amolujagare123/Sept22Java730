package InheritanceDemo.Polymorphism;

public class Casting {

    public static void main(String[] args) {

        int a = 10;
        int b = 3;

        double c = (double) a/b;

        // if you want to store one type of value in another type then
        // put that type in front of the value for e.g. (double)

        System.out.println(c);

    }
}
